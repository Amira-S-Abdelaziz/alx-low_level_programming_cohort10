#include <stdio.h>
#include "3-calc.h"
/**
*op_add - function
*@a: var
*@b: var
*Return: value
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
*op_sub - function
*@a: var
*@b: var
*Return: value
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
*op_mul - function
*@a: var
*@b: var
*Return: value
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
*op_div - function
*@a: var
*@b: var
*Return: value
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
*op_mod - fucntion
*@a: var
*@b: var
*Return: value
*/
int op_mod(int a, int b)
{
	return (a % b);
}
